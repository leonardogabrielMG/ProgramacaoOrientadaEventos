    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        Fisioterapeuta fisioterapeuta = new Fisioterapeuta(campoNome.getText(), converteStringDate(campoNascimento.getText()), 
                comboGenero.getSelectedItem().toString(), campoCPF.getText(), campoTelefone.getText(), 
                campoCrefito.getText(), cmapoEspecialidade.getText());
        
        DAOFisioterapeuta cadastrar = new DAOFisioterapeuta();
        
        if (cadastrar.insereFisioterapeuta(fisioterapeuta)) {
            JOptionPane.showMessageDialog(null,campoNome.getText() + " cadastrado(a) com sucesso.");
            limpar();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar " + campoNome.getText() + "! O programa será finzalizado.");
            dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

