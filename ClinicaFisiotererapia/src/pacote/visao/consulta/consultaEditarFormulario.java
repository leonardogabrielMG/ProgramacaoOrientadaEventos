    public consultaEditarFormulario(Consulta consulta) {
        initComponents();
        
        String[] auxDataNascimentoVetor = converteDataString(consulta.getData()).split("/");
        String auxDataNascimento = "";
        
        for (int i = 0; i < auxDataNascimentoVetor.length; i++) {
            auxDataNascimento += auxDataNascimentoVetor[i];
        }
        
        DAOConsulta pessoas = new DAOConsulta();
        
        DefaultComboBoxModel modeloFisioterapeuta = new DefaultComboBoxModel(pessoas.buscarFisioterapeuta().toArray());
        comboFisioterapeuta.setModel (modeloFisioterapeuta);
        
        DefaultComboBoxModel modeloPaciente = new DefaultComboBoxModel(pessoas.buscarPaciente().toArray());
        comboPaciente.setModel (modeloPaciente);
        
        campoId.setText(consulta.getIdConsulta().toString());
        comboPaciente.setSelectedItem(consulta.getNomePaciente());
        comboFisioterapeuta.setSelectedItem(consulta.getNomeFisioterapeuta());
        campoData.setText(auxDataNascimento);
        comboStatus.setSelectedItem(consulta.getStatus().toString());
        areaDescricao.setText(consulta.getDescricao());
    }

