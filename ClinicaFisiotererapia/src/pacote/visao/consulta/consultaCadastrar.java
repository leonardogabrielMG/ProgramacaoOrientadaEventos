    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        Consulta consulta = new Consulta(comboPaciente.getSelectedItem().toString(), comboFisioterapeuta.getSelectedItem().toString(), converteStringDate(campoData.getText()), 
                areaDescricao.getText(), comboStatus.getSelectedItem().toString());
        
        DAOConsulta inserir = new DAOConsulta();
        
        if (inserir.insereConsulta(consulta)) {
            JOptionPane.showMessageDialog(null,"Consulta de " + comboPaciente.getSelectedItem().toString() + " agendada com sucesso!");
            limpar();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar consulta de " + comboPaciente.getSelectedItem().toString() + "! O programa será finalizado.");
            dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

