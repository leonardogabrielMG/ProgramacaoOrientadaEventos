    public void limpar(){
        campoCrefito.setText("");
        cmapoEspecialidade.setText("");
        campoNome.setText("");
        campoNascimento.setText("");
        comboGenero.setSelectedIndex(0);
        campoCPF.setText("");
        campoTelefone.setText("");
    }
    
    public static Date converteStringDate(String data) 
    {
        DateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
        Date novaData = null;
        try {
            novaData = formatado.parse(data);
        } catch (ParseException ex) {
            Logger.getLogger(fisioterapeutaCadastrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        return novaData;
    }

