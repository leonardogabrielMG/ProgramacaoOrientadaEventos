    public void limpar(){
        comboPaciente.setSelectedIndex(0);
        comboFisioterapeuta.setSelectedIndex(0);
        campoData.setText("");
        comboStatus.setSelectedIndex(0);
        areaDescricao.setText("");
    }
    
    public static Date converteStringDate(String data) 
    {
        DateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
        Date novaData = null;
        try {
            novaData = formatado.parse(data);
        } catch (ParseException ex) {
            Logger.getLogger(consultaCadastrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        return novaData;
    }

