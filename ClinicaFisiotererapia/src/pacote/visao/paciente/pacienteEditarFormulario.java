    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // TODO add your handling code here:
        Paciente paciente = new Paciente(campoNome.getText(), converteStringDate(campoNascimento.getText()), 
                comboGenero.getSelectedItem().toString(), campoCPF.getText(), campoTelefone.getText(), campoConvenio.getText());
        paciente.setIdPaciente(Integer.parseInt(campoID.getText().toString()));
        
        DAOPaciente editar = new DAOPaciente();
        
        if (editar.alteraPaciente(paciente)) {
            JOptionPane.showMessageDialog(null,campoNome.getText() + " alterado(a) com sucesso.");
            dispose();
            new pacienteEditar().setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao editar " + campoNome.getText() + "! O programa será finzalizado.");
            dispose();
        }
    }//GEN-LAST:event_btEditarActionPerformed

