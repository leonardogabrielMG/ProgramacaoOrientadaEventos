    public pacienteEditarFormulario(Paciente paciente) {
        initComponents();
        
        String[] auxDataNascimentoVetor = converteDataString(paciente.getNascimento()).split("/");
        String auxDataNascimento = "";
        
        for (int i = 0; i < auxDataNascimentoVetor.length; i++) {
            auxDataNascimento += auxDataNascimentoVetor[i];
        }
        
        campoNome.setText(paciente.getNome());
        campoNascimento.setText(auxDataNascimento);
        comboGenero.setSelectedItem(paciente.getGenero());
        campoCPF.setText(paciente.getCpf());
        campoTelefone.setText(paciente.getTelefone());
        campoID.setText(paciente.getIdPaciente().toString());
        campoConvenio.setText(paciente.getConvenio());
    }

