    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // TODO add your handling code here:
        Consulta consulta = new Consulta(comboPaciente.getSelectedItem().toString(), comboFisioterapeuta.getSelectedItem().toString(), converteStringDate(campoData.getText()), 
                areaDescricao.getText(), comboStatus.getSelectedItem().toString());
        consulta.setIdConsulta(Integer.parseInt(campoId.getText()));
        
        DAOConsulta editar = new DAOConsulta();
        
        if (editar.alteraConsulta(consulta)) {
            JOptionPane.showMessageDialog(null,"Consulta de " + comboPaciente.getSelectedItem().toString() + " alterada com sucesso!");
            dispose();
            new consultaEditar().setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao alterar consulta de " + comboPaciente.getSelectedItem().toString() + "! O programa será finalizado.");
            dispose();
        }
    }//GEN-LAST:event_btEditarActionPerformed

